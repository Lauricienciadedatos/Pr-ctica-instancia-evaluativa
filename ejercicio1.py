def mensaje_aula(num_aula):
    print(f"Hola Aula {num_aula}, ¿Qué tal?")
    print("¡Espero que estén teniendo un buen día!")
    print("¿Estan preparados para ejercitar hoy con Python?")
 

# Intercambio de mensajes entre tres aulas
for aula in range(1, 4):
    mensaje_aula(aula)
  
    